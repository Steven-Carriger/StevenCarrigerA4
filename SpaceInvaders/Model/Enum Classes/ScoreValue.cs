namespace SpaceInvaders.Model.Enum_Classes
{
    /// <summary>
    ///     The possible score values for the enemy ships
    /// </summary>
    public enum ScoreValue
    {
        /// <summary>
        ///     The default score value for a ship
        /// </summary>
        Default = 100,

        /// <summary>
        ///     The average score value for a ship
        /// </summary>
        AverageValue = 250,

        /// <summary>
        ///     The advance score value for a ship
        /// </summary>
        AdvanceValue = 500,

        /// <summary>
        ///     The challenging score value for a ship
        /// </summary>
        ChallengingValue = 650
    }
}